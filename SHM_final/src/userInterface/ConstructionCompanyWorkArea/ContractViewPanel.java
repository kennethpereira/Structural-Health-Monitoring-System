    private void btnDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDataActionPerformed
        // TODO add your handling code here:
        
        int row=tblContract.getSelectedRow();

        if(row<0){
            JOptionPane.showMessageDialog(this, "Select a row");
            return;
        }
        ContractRequest contract=(CompanyContract)tblContract.getValueAt(row, 0);
        
        CompanyContract contract1=(CompanyContract)tblContract.getValueAt(row, 0);
        if(account==contract.getCompany()&&!contract1.getStatus().equals("Completed")){
            contract1.setStatus("Under Progress");
        
        EmailManagementSystem email=municipalCorporation.getEmail();
        
        String to=contract.getSender().getPerson().getEmailID();
        String subject="Contract Request Under Process";
        String Body="Contract is now under Process";
        
        try {
            email.GenerateMail(to, subject, Body);
        } catch (MessagingException ex) {
            
        }
        
        ProcessContractJPanel cvj=new ProcessContractJPanel(userProcessContainer,municipalCorporation,contract);
        userProcessContainer.add("ProcessContractJPanel", cvj);
        
        CardLayout layout=(CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else{
            
            JOptionPane.showMessageDialog(this, "Contract not available");
        }
        
    }//GEN-LAST:event_btnDataActionPerformed

