    public void populateTable(){
        
        DefaultTableModel dtm=(DefaultTableModel)tblContract.getModel();
        dtm.setRowCount(0);
        
        for(ContractRequest request : organization.getWorkQueue().getContractList()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1]=request.getCompany();
            row[2] = request.getArchitecture();
            row[3] = ((CompanyContract)request).getStatus();
            
            
            dtm.addRow(row);
        }
    }
