    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
       try{
        String name =txtStateName.getText();
        String Regex = "^[a-zA-Z]+$";
        
        if(!name.trim().matches(Regex)){
            JOptionPane.showMessageDialog(this, "Please enter a vlaid State name!!.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   
            
        }
        else{
             StateGoverment state=fedGoverment.createAndAddStateGoverment();
        
        state.setName(name);
        
        JOptionPane.showMessageDialog(this,"New State Added to the System");
        populateTable();
        txtStateName.setText("");
       
        }
       }
       catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please enter a vlaid State name!!.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   
           
       }
        
    }//GEN-LAST:event_btnSubmitActionPerformed
   
