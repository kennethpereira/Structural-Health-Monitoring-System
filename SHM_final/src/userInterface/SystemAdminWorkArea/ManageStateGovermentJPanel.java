    public void populateTable(){
        
        DefaultTableModel dtm=(DefaultTableModel) stateTable.getModel();
        
        dtm.setRowCount(0);
        
        for(StateGoverment state:fedGoverment.getStateGovermentList()){
            
            Object row[]=new Object[1];
            
            row[0]=state.getName();
            dtm.addRow(row);
        }
        
    }

