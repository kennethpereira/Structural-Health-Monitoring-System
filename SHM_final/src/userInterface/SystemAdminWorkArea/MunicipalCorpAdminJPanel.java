    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        try{
        MunicipalCorporation enterprise = (MunicipalCorporation) departmentCombo.getSelectedItem();
        
       
        String username = fldUserName.getText();
        String password = String.valueOf(fldPassword.getPassword());
        String name = fldName.getText();
        String regEx1="^[a-zA-Z0-9][0-9a-zA-Z .,'-]*$";
        String Regex = "^[a-zA-Z]+$";
        
        if(!name.trim().matches(Regex)&&!password.trim().matches(regEx1)&&!username.trim().matches(regEx1)){
            JOptionPane.showMessageDialog(this, "Please Enter valid data into the fields.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   
            
        }
        else{
            Person employee = enterprise.getPersonDirectory().createPerson(name,"");
        
        UserAccount account = enterprise.getUserAccountDirectory().createUserAccount
        (username, password, employee, new MunicipalCorpAdminRole());
        
        JOptionPane.showMessageDialog(this, "Municipal Corporation Admin added");
        
        populateTable();
        
        fldName.setText("");
        fldPassword.setText("");
        fldUserName.setText("");
        }
        
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please Enter valid data into the fields.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   
            
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

