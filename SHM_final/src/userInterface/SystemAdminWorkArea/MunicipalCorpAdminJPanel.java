    public void populateTable(){
        
        DefaultTableModel dtm=(DefaultTableModel) systemTable.getModel();
        dtm.setRowCount(0);
        for(StateGoverment state:fedGoverment.getStateGovermentList()){
            for(MunicipalCorporation dept:state.getMunicipalCorporationDirectory().getMunicipalCorporationList()){
                for(UserAccount ua:dept.getUserAccountDirectory().getUserAccountList()){
                
                Object row[]=new Object[3];
                
                row[0]=state;
                row[1]=dept.getName();
                row[2]=ua.getUsername();
                
                dtm.addRow(row);
                }
                
            }
        }
        
    }
    
    public void populateCombo(){
        
        stateCombo.removeAllItems();
        
        
        for(StateGoverment state:fedGoverment.getStateGovermentList()){
            if(state!=null){
              stateCombo.addItem(state);
              btnSubmit.setEnabled(true);
            }
            
            
        }
    }
