    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        try{
            Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
        String name = fldName.getText();
        String mail=fldEmail.getText();
        String Regex = "^[a-zA-Z]+$";
        String emailRegEx="^[_A-Za-z0-9-\\\\+]+(\\\\.[_A-Za-z0-9-]+)*\n" +
                    "@[A-Za-z0-9-]+(\\\\.[A-Za-z0-9]+)*(\\\\.[A-Za-z]{2,})$;";
        
        if(!name.trim().matches(Regex)&&!mail.trim().matches(emailRegEx))
        {
            JOptionPane.showMessageDialog(this, "Please enter valid name and email.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   

        }
        else{
                organization.getPersonDirectory().createPerson(name,mail);
        
        
        
        JOptionPane.showMessageDialog(this, name+" added to the "+organization);
       // populateTable(organization);
        
        fldName.setText("");
        fldEmail.setText("");
            }
        
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(this, "Please enter valid name and email.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   

        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

