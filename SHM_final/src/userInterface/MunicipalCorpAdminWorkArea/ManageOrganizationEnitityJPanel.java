    public void populateOrganizationPersonComboBox(){
        organizationEmpJComboBox.removeAllItems();
        
        for (Organization organization : organizationDirectory.getOrganizationList()){
            if(organization!=null){
               organizationEmpJComboBox.addItem(organization);
               btnSubmit.setEnabled(true);
            }
            
        }
    }
    
    public void populateOrganizationComboBox(){
        organizationCombo.removeAllItems();
        
        for (Organization organization : organizationDirectory.getOrganizationList()){
            organizationCombo.addItem(organization);
        }
    }
    
    public void populateTable(Organization organization){
        DefaultTableModel model = (DefaultTableModel) organizationTable.getModel();
        
        model.setRowCount(0);
        
        for (Person person : organization.getPersonDirectory().getEmployeeList()){
            Object[] row = new Object[3];
            row[0] = person.getId();
            row[1] = person.getName();
            row[2]=person.getEmailID();
            model.addRow(row);
        }
            
        
    }

