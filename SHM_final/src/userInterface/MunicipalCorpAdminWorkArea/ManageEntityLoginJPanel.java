    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        try{
           String userName = fldUserName.getText();
        String password = String.valueOf(fldPassword.getPassword());
        String regEx="^[a-zA-Z0-9][0-9a-zA-Z .,'-]*$";
        Organization organization = (Organization) organizationCombo.getSelectedItem();
        Person person = (Person) personCombo.getSelectedItem();
        Role role = (Role) roleCombo.getSelectedItem();
        if(!userName.trim().matches(regEx)&&!password.trim().matches(regEx)){
            JOptionPane.showMessageDialog(this, "Please add correct data in the fields.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   

        }
        else{
            organization.getUserAccountDirectory().createUserAccount(userName, password, person, role);
        
        EmailManagementSystem email=municipalCorp.getEmail();
        String to=person.getEmailID();
        String subject="Welcome!!";
        String body="Welcome to the system \n Your userName is "+userName;
        
        try {
            email.GenerateMail(to, subject, body);
        } catch (MessagingException ex) {
            JOptionPane.showMessageDialog(this, "Error while sending the email ");
        }
        populateTable();
            
        } 
        JOptionPane.showMessageDialog(this, "UserName and Password added to the system.");
        fldPassword.setText("");
        fldUserName.setText("");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please add correct data in the fields.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);   

        }
       
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    public void populatePerson(Organization organization){
        personCombo.removeAllItems();
        
        for (Person person : organization.getPersonDirectory().getEmployeeList()){
            personCombo.addItem(person);
        }
    }
    
    public void populateRole(Organization organization){
        
        roleCombo.removeAllItems();
        for (Role role : organization.getSupportedRole()){
            
            roleCombo.addItem(role);
        }
        
    }

