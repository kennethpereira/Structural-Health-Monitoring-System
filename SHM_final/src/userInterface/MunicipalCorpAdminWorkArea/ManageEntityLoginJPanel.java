    public void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) systemTable.getModel();

        model.setRowCount(0);

        for (Organization organization : municipalCorp.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = organization;
                row[1] = ua;
                ((DefaultTableModel) systemTable.getModel()).addRow(row);
            }
        }
    }
    
    public void populateOrganizationCombo(){
        
        organizationCombo.removeAllItems();

        for (Organization organization : municipalCorp.getOrganizationDirectory().getOrganizationList()) {
            if(organization!=null){
                organizationCombo.addItem(organization);
                btnSubmit.setEnabled(true);
            }
            
        }
    }
